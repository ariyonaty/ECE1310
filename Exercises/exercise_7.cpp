#include <iostream>

using namespace std;

int main()
{
    int z = 0, a = 1, b = 2, c = 3, d = 4, e = 5, f = 6, g = 7, h = 8, i = 9, j = 10;

    cout << "Squares and cubes for range of 0 to 10.\n\n";
    cout << "Integer \tSquare \t\tCube\n";
    cout << z << "\t\t" << z * z << "\t\t" << z * z * z << "\n";
    cout << a << "\t\t" << a * a << "\t\t" << a * a * a << "\n";
    cout << b << "\t\t" << b * b << "\t\t" << b * b * b << "\n";
    cout << c << "\t\t" << c * c << "\t\t" << c * c * c << "\n";
    cout << d << "\t\t" << d * d << "\t\t" << d * d * d << "\n";
    cout << e << "\t\t" << e * e << "\t\t" << e * e * e << "\n";
    cout << f << "\t\t" << f * f << "\t\t" << f * f * f << "\n";
    cout << g << "\t\t" << g * g << "\t\t" << g * g * g << "\n";
    cout << h << "\t\t" << h * h << "\t\t" << h * h * h << "\n";
    cout << i << "\t\t" << i * i << "\t\t" << i * i * i << "\n";
    cout << j << "\t\t" << j * j << "\t\t" << j * j * j << "\n";
}